// ソート済み辞書キャッシュ（geocache統合後にbuildDictCacheで再構築）
let dictCache = null;

export function buildDictCache() {
  dictCache = Object.keys(LOCATION_COORDS)
    .filter(k => !LOCATION_BLOCKLIST.has(k))
    .sort((a, b) => b.length - a.length)
    .map(key => ({
      key,
      re: new RegExp(`\\b${key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'i'),
    }));
}

export function extractLocationFromDict(text) {
  if (!dictCache) buildDictCache();
  for (const { key, re } of dictCache) {
    if (re.test(text)) {
      return { coords: LOCATION_COORDS[key], name: LOCATION_LABELS[key] || key };
    }
  }
  return null;
}
